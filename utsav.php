            <p>“Utsav” is one of the most important day for our orphanage, especially for our young ones. It is celebrated on 14th Nov. On this very special day, we celebrate our kids' birthdays. On this annual day, the orphanage celebrates its success and achievements. The achievements of all , children, inters and our hardworking care takers is being well appreciated on this day. Generally, this celebration includes cultural programs, dance and singing performance. We invite eminent personalities of administrative fraternity like trustees or politicians from the Education Ministry or from local government bodies as chief guests for this enchanting celebration.</p>
            <p>UTSAV is celebrated on children's Day to celebrate the spirit of every child. Different organizations , NGO's and our precious donors make this day more promising and cherishing for Vision children. Vision is celebrating “ UTSAV” since 2005 and this is our 14th celebration with lots of achievements and also a promising future for our kids.</p>
            <p>In UTSAV our orphanage caretakers play a significant role. They contribute to each and every activity performed in UTSAV. They help children to prepare their acts and work on every fine detail of each child's act. They help to let each child showcase his talent to the world . The role of volunteers and interns is also a crucial one. They work under the guidance of caretaker to make UTSAV memorable for all, who became a part of this as audience or participant.</p>
            
